// 返回 text 中命中的禁用词（去重，按出现顺序）
export function findBannedWords(text: string): string[] {
  if (!text) return [];
  const found: string[] = [];
  for (const word of BANNED_WORDS) {
    if (word && text.includes(word) && !found.includes(word)) found.push(word);
  }
  return found;
}
